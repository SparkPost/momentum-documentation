<div class="titlepage"><div><div><h2 class="title" style="clear: both">
<a name="modules.mxip"></a>71.49. mxip - IP Addresses In MX Records</h2></div></div></div>
<a class="indexterm" name="idp22302992"></a><p class="config"><b>Configuration Change. </b>
      This feature is available in Momentum 4.2 and later.
    </p>
<p>
    Several domains on the internet have configured their MX records so
    that the hostname field contains a textual IP address. This is
    against the DNS standard RFC 1035
    <a class="link" href="http://www.rfc-editor.org/rfc/rfc1035.txt" target="_top">Domain
    Names - Implementation and Specification</a>, which says that MX
    records must contain a domain name, not an IP address.
    Unfortunately, many domains ignore the standard.
  </p>
<pre class="programlisting">
3.3.9. MX RDATA format

    +--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+
    |                  PREFERENCE                   |
    +--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+
    /                   EXCHANGE                    /
    /                                               /
    +--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+

where:

PREFERENCE      A 16 bit integer which specifies the preference given to
                this RR among others at the same owner.  Lower values
                are preferred.

EXCHANGE        A &lt;domain-name&gt; which specifies a host willing to act as
                a mail exchange for the owner name.
  </pre>
<div class="section">
<div class="titlepage"><div><div><h3 class="title">
<a name="modules.mxip.configuration"></a>71.49.1. Configuration</h3></div></div></div>
<p>
      It is possible to configure the bind nameserver, and perhaps other
      nameservers, with an IP address in this field.
    </p>
<div class="example">
<a name="modules.mxip.test.domain.example"></a><p class="title"><b>Example 71.73. Example Configuration - Test Domain</b></p>
<div class="example-contents"><pre class="programlisting">
dig mx broken.mxip.example @127.0.0.1

; &lt;&lt;&gt;&gt; DiG 9.3.6-P1-RedHat-9.3.6-4.P1.el5_4.2 &lt;&lt;&gt;&gt; mx broken.mxip.example @127.0.0.1
;; global options:  printcmd
;; Got answer:
;; -&gt;&gt;HEADER&lt;&lt;- opcode: QUERY, status: NOERROR, id: 49365
;; flags: qr aa rd ra; QUERY: 1, ANSWER: 1, AUTHORITY: 1, ADDITIONAL: 0

;; QUESTION SECTION:
;broken.mxip.example.		IN	MX

;; ANSWER SECTION:
broken.mxip.example.	86400	IN	MX	10 10.79.0.143.

;; AUTHORITY SECTION:
mxip.example.		86400	IN	NS	localhost.

;; Query time: 3 msec
;; SERVER: 127.0.0.1#53(127.0.0.1)
;; WHEN: Fri Mar  5 00:23:02 2010
;; MSG SIZE  rcvd: 87
   </pre></div>
</div>
<br class="example-break"><p>
      The mxip module implements a dns_get_As hook in order to augment
      the DNS resolution behaviour. If your integration or deployment
      also implements a dns_get_As hook, then you may not be able to use
      the mxip module.
    </p>
<p>
      The mxip module only supports IPv4 addresses in the hostname field
      of MX records. IPv6 addresses are explicitly not supported by the
      mxip module.
    </p>
<p>
      The mxip module can be configured as follows:
    </p>
<pre class="programlisting">
mxip
{
  # The module creates a "fake" hostname for the IP address
  # in the MX record. It gives these "fake" hostnames
  # a TTL, which defaults to 600 seconds (10 minutes).
  # The TTL can be configured via the TTL option (see below).
  #
  # Note that the TTL of the "fake" hostnames respects
  # the Min_DNS_TTL and Max_DNS_TTL options. See:
  #
  # &lt;https://support.messagesystems.com/docs/web/conf.ref.min_dns_ttl.php&gt;
  # &lt;https://support.messagesystems.com/docs/web/conf.ref.max_dns_ttl.php&gt;
  #
  # TTL = "600"
}
   </pre>
<p>
      The mxip module will log information at the INFO level, when it
      sees an IP address in the MX record. You can enable this at
      run-time using the "debug" command:
    </p>
<pre class="programlisting">
debug module mxip INFO
   </pre>
<p>
      If you want to run with it enabled permanently, you can do that
      via the module configuration:
    </p>
<pre class="programlisting">
mxip
{
  Debug_Level = "INFO"
}
   </pre>
